package com.ifrj.tcc.lembre.Entidades;

public class Cartas {

    private String frente;
    private String verso;
    private String imgFrente;
    private String imgVerso;

    public Cartas() {    }

    public String getFrente() {
        return frente;
    }

    public void setFrente(String frente) {
        this.frente = frente;
    }

    public String getVerso() {
        return verso;
    }

    public void setVerso(String verso) {
        this.verso = verso;
    }

    public String getImgFrente() {
        return imgFrente;
    }

    public void setImgFrente(String imgFrente) {
        this.imgFrente = imgFrente;
    }

    public String getImgVerso() {
        return imgVerso;
    }

    public void setImgVerso(String imgVerso) {
        this.imgVerso = imgVerso;
    }
}
